static inline const uint8_t *get_table_from_polynom(enum al_crc8_polynom polynom)
{
	(void)polynom;

	/*
	 * since the only polynom currently supported is CRC 8N
	 * simply return it.
	 */
	return crc_8N_table;
}


/**
 * Generic 8 bit CRC calculation function.
 *
 * @param       initial_crc
 *              the crc value of previous block
 * @param       data
 *              the data to calculate its crc
 *              if set to NULL, the 'initial_crc' is returned
 * @param       size
 *              number of bytes in 'data' buffer
 *              if set to 0, the 'initial_crc' is returned
 * @param       polynom
 *              the polynom to use for CRC calculation
 * @returns     crc result value
 */
uint8_t al_crc8(uint8_t initial_crc,
		const uint8_t *data,
		uint32_t size,
		enum al_crc8_polynom polynom)
{
	/* the default returned value is the initial value */
	uint8_t crc = initial_crc;

	/* get the corresponding table to our polynom */
	const uint8_t * const crc_table = get_table_from_polynom(polynom);

	/* verify both data and size are specified */
	if (data && size) {

		/* pointer to the end of the data */
		const uint8_t * const data_end = data + size;

		/* traverse all data bytes */
		while (data < data_end)
			crc = crc_table[crc ^ *data++];
	}

	return crc;
}
